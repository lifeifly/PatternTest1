package com.yele.hu.upgradetools.bean.comfig.car;

public class MeterStyleConfig {

    /**
     * <风格切换>：表示要切换到的风格编号。为空表示不进行更改。
     */
    public int interfaceStyle;

    /**
     * 显示语种切换：表示要切换的语种，
     *             0：中文
     *             1：英文
     */
    public int showLanguage;

    /**
     * 显示模式：表示表示仪表显示模式，
     *         0：日间
     *         1：夜间
     */
    public int mode;

}
